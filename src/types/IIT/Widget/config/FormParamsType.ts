export interface FormParamsType {
  /**
   * Зчитувати ос. ключі тільки свого ЦСК (першого в CAs.json).
   * Діалог вибору ЦСК не відображається
   */
  ownCAOnly?: boolean;

  /**
   * Відображати інформацію про зчитаний ос. ключ
   */
  showPKInfo?: boolean;

  /**
   * Відображати крок з інформацією про рекомендуємий тип підпису
   */
  showSignTip?: boolean;

  /**
   * Мова інтерфейсу віджету (підтримується українська - ua, англійська - en)
   */
  language?: string;
}
